    private void jButtonSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveChangesActionPerformed
        String naziv = jTextFieldNaziv.getText();
        String zvanje = jTextFieldZvanje.getText();
        boolean valid = true;
        jLabelNaziv.setText("");
        jLabelZvanje.setText("");
        if(naziv.isEmpty()){
            jLabelNaziv.setText("Unesite naziv");
            valid=false;
        }
        if(zvanje.isEmpty()){
            jLabelZvanje.setText("Unesite zvanje");
            valid=false;
        }
        if(!valid)
            return;
        
        l.setNazivLicence(naziv);
        l.setZvanjeInstruktora(zvanje);
        try {
            if(Controller.getInstance().promeniLicenca(l))
                JOptionPane.showMessageDialog(parent, "Uspešno ažurirana licenca.","Uspešno ažuriranje",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
        } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Greška prilikom ažuriranja licence. \n"+ex.getMessage(),"Greška",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveChangesActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int i =JOptionPane.showConfirmDialog(parent, "Da li ste sigurni da želite da obrišete licencu?","Brisanje licence",JOptionPane.YES_NO_OPTION);
        if(i == JOptionPane.YES_OPTION){
            try{
            boolean b = Controller.getInstance().obrisiLicenca(l);
            if(b)
                JOptionPane.showMessageDialog(parent, "Uspešno obrisana licenca.","Uspešno brisanje",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(rootPane, "Greška prilikom brisanja licence. \n"+ex.getMessage(),"Greška",JOptionPane.ERROR_MESSAGE);
            }
            }else{
            return;
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        String naziv = jTextFieldNaziv.getText();
        String zvanje = jTextFieldZvanje.getText();
        boolean valid = true;
        jLabelNaziv.setText("");
        jLabelZvanje.setText("");
        if(naziv.isEmpty()){
            jLabelNaziv.setText("Unesite naziv");
            valid=false;
        }
        if(zvanje.isEmpty()){
            jLabelZvanje.setText("Unesite zvanje");
            valid=false;
        }
        if(!valid)
            return;
        Licenca l = new Licenca();
        l.setNazivLicence(naziv);
        l.setZvanjeInstruktora(zvanje);
        
        try {
            if(Controller.getInstance().kreirajLicenca(l))
                JOptionPane.showMessageDialog(parent, "Uspešno kreirana licenca.","Uspešno kreiranje",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
        } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Greška prilikom kreiranja licence. \n"+ex.getMessage(),"Greška",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

